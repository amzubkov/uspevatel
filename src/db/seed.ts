async function loadImageBlob(exerciseId: number): Promise<Uint8Array | null> {
  const asset = exerciseImages[exerciseId];
  if (!asset) return null;
  try {
    const resolved = Asset.fromModule(asset);
    await resolved.downloadAsync();
    if (!resolved.localUri) return null;
    const base64 = await FileSystem.readAsStringAsync(resolved.localUri, { encoding: 'base64' });
    const binary = atob(base64);
    const bytes = new Uint8Array(binary.length);
    for (let i = 0; i < binary.length; i++) bytes[i] = binary.charCodeAt(i);
    return bytes;
  } catch {
    return null;
  }
}

