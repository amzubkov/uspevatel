/** Backfill image_data for all preset exercises that don't have it yet */
export async function backfillExerciseImages(db: SQLiteDatabase) {
  const rows = await db.getAllAsync<{ id: number }>('SELECT id FROM exercises WHERE is_preset = 1 AND image_data IS NULL');
  for (const row of rows) {
    const blob = await loadImageBlob(row.id);
    if (blob) {
      await db.runAsync('UPDATE exercises SET image_data = ? WHERE id = ?', [blob, row.id]);
    }
  }
}
